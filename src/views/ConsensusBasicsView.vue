    <h3>Decision Point Structure</h3>
    <h5>Agreement (Thumbs Up)</h5>
    <p class="bullet1">Basic alignment with group direction and proposal</p>
    <p class="bullet1">
      Consensus is not the same thing as 100% unanimous vote in majority rule
    </p>
    <p class="bullet1">
      Standard of how much agreement is enough may vary (depending on group and
      item)&mdash;how much agreement is sufficient to achieve solid
      implementation of this proposal? May range from "This is fabulous" to "I
      can live with it for now"
    </p>
    <h5>Standing Aside (Thumbs Sideways)</h5>
    <p class="bullet1">
      A choice to let the group proceed, while personally not feeling aligned
      with direction, based on:
    </p>

    <ul class="bullet1">
      <li>personal values or principles</li>
      <li>
        personal impact or need, e.g. "I can't afford this" or "I'd have to move
        out"
      </li>
      <li>disagreement with group direction</li>
      <li>intuition, not in alignment</li>
    </ul>
    <p class="bullet1">Name in minutes and reason</p>
    <p class="bullet1">
      Not in lead implementation role, but still bound by decision
    </p>
    <p class="bullet1">
      How many is too many? Probably more than 1 (or at most 2). The more you
      allow, the more it's like voting; go too far and you lose the extra
      effectiveness of consensus action.
    </p>
    <h5>Blocking (Thumbs Down)</h5>
    <p class="bullet1">
      Don't do prematurely. Integrate your concerns into discussion early on.
      The following are not valid reasons to block:
    </p>

    <ul class="bullet1">
      <li>
        to get your way or because you prefer a different proposal, or no
        proposal
      </li>
      <li>to fulfill your personal moral values or how you want to live</li>
      <li>tradition; because things have always been done this way</li>
      <li>
        because the proposed action doesn't fit your personal needs (or
        finances)
      </li>
      <li>because you'd have to leave the group if the proposal passed</li>
      <li>to prevent the group from taking a risk</li>
    </ul>
    <p class="bullet1">
      Only do if proposal is disastrous for the group or crosses group's known
      core principles&mdash;then responsible for stopping it
    </p>
    <p class="bullet1">
      Legitimate blocks are most likely to arise from different interpretations
      of a group value, or two group values coming into conflict with each other
    </p>
    <p class="bullet1">
      Inappropriate blocking being the most common cause of dysfunctional
      consensus process, groups benefit from having both cultural and procedural
      ways to address this behavior
    </p>
    <p class="bullet1">Culture-creation may include:</p>

    <ul class="bullet1">
      <li>
        train incoming members in consensus, including what constitutes an
        appropriate block
      </li>
      <li>
        reinforce that anyone who blocks is responsible for helping to work out
        an alternative
      </li>
      <li>
        idea that half a dozen blocks for all your groups is lifetime quota
      </li>
      <li>
        don't allow blocks to prevent the group from continuing to talk about
        the issue
      </li>
    </ul>
    <p class="bullet1">Procedural responses to blocks may include:</p>
    <ul class="bullet1">
      <li>facilitator overrule</li>
      <li>
        refer unresolved blocks to coordinating committee/steering council for
        possible overrule
      </li>
      <li>only counts if rest of group agrees it's principled</li>
      <li>
        only counts if at least one other group member agrees it's principled
      </li>
      <li>fallback to super-majority vote</li>
      <li>
        requirement that blocker organize meetings with others to work out an
        alternative solution, otherwise group can proceed
      </li>
    </ul>

