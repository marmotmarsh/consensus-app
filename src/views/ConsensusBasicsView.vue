      Consensus is a cooperative process in which group members develop and
      agree to support a decision in the best interest of the whole.
    </h5>
    <p>
      <strong>
        Main reasons to use consensus as a decision-making method:
      </strong>
    </p>
    <ol>
      <li>High quality decisions</li>
      <li>Builds connection among members</li>
      <li>More effective implementation</li>
    </ol>

    <h3>Principles</h3>
    <p class="normal-header">
      <em>
        <strong>
          Cooperation is the basis (we all get more of what we want when we
          cooperate)
        </strong>
      </em>
    </p>
    <p class="bullet1">
      Consensus is for people who are genuinely trying to work together,
      typically based on some shared interest, purpose or calling; OK if some
      interests are divergent, as long as sufficient joint interests are present
    </p>
    <p class="bullet1">
      Search together for the best solution for the group: it's "us against the
      problem" we are trying to solve rather than "us against each other"
    </p>
    <p class="bullet1">
      Recognize that no decision-making system gets everyone their first choice
      all the time
    </p>
    <p class="bullet1">
      What's the creative way to address all the needs present, rather than
      lowest common denominator Skills necessary for consensus are also
      necessary for good relationships
    </p>
    <p class="normal-header">
      <em>
        <strong>Consensus seeks to synthesize the wisdom of the group</strong>
      </em>
    </p>
    <p class="bullet1">
      Unity (different from unanimous vote or everyone's first choice) "Sense of
      the meeting"&mdash;essence is something you can support or are willing to
      let go forward, I can live with it
    </p>
    <p class="bullet1">"Everyone has a piece of the truth"</p>
    <p class="bullet1">
      Value all kinds of input (rational, emotional, kinesthetic, etc.)
    </p>
    <p class="bullet1">
      Consensus is a questioning process, more than an affirming process
    </p>
    <p class="bullet1">
      Goal is to thrash out an issue until a good solution is found, rather than
      make everyone feel good by a fast but fake agreement
    </p>
    <p class="bullet1">
      Encourages lifting everyone up to their highest potential, instead of
      knocking down your opponents
    </p>
    <p class="bullet1">
      Share, question, and learn from each other's experience and thinking
    </p>
    <p class="normal-header">
      <em>
        <strong>
          Remember the spirit and process of the system are even more important
          than the structures
        </strong>
      </em>
    </p>
    <p class="bullet1">
      There is no substitute for being friends with each other
    </p>
    <p class="bullet1">A positive attitude will get you everywhere</p>
    <p class="bullet1">
      Meetings should be fulfilling&mdash;if they're not, then ask why, and
      change it!
    </p>
    <h3>What Helps Consensus Work Well</h3>
    <ul>
      <li>common purpose</li>
      <li>listening</li>
      <li>
        openness to ideas, feelings and experiences of others&mdash;&gt;
        transformative experience, give & take
      </li>
      <li>trust (some degree)</li>
      <li>
        willingness to let go of some of your personal attachments in the best
