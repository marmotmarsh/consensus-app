    <strong>
      1. Introduction to Issue (by presenter, preferably not the facilitator)
    </strong>
    <p class="bullet1">Why are we talking about this, why does it matter</p>
    <p class="bullet1">History of the issue (including previous meetings)</p>
    <p class="bullet1">
      Goal for this item at this particular meeting (report, decision, committee
      gather input, etc.) Offer as issue or proposal? It depends on the issue.
      For example:
    </p>
    <p class="bullet2">
      If small and simple, proposal may pass with relative ease
    </p>
    <p class="bullet2">
      If small and complex, likely send to committee for main work
    </p>
    <p class="bullet2">
      If large and simple, might be ok to start with proposal, and might take a
      few more meetings, possibly with some committee work in between
    </p>
    <p class="bullet2">
      If large and complex, start with issue not proposal, expect it to take a
      series of meetings, almost always with committee or individual work in
      between
    </p>
    <p class="bullet1">
      At end of initial presentation, others who have factual knowledge of the
      issue are sometimes invited to add in further bits about the history and
      so on&mdash;but don't let this go on too long
    </p>
    <strong>2. Clarifying Questions</strong>
    <p class="bullet1">Questions of understanding only&mdash;short and sweet</p>
    <strong>3. Discussion</strong>
    <p class="bullet1">Further questions</p>
    <p class="bullet1">
      Bring out diversity of ideas, concerns, and perspectives
    </p>
    <p class="bullet1">
      Deal first with values concerns or big picture, before getting into
      details
    </p>
    <p class="bullet1">
      Note agreements and disagreements on general direction and the underlying
      reasons for them&mdash;discuss those underlying reasons and needs
    </p>
